{
	fg_pen_mode = fg_pen;
	bg_pen_mode = bg_pen;
}

void cpct_drawCharM0(void* video_memory, u16 ascii)
{
	if (wincpct_isCpcMem(video_memory))
		video_memory = wincpct_getMemory((int)video_memory);

	wincpct_displayFontM0(video_memory, fg_pen_mode, bg_pen_mode, (char)ascii);
}

